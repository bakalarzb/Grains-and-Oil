                <h3>1. Renewable Energy Sources</h3>
                <p>It’s quite common for us to think about energy consumption and sources when we think about sustainability. Mainstream media covers climate change in great detail, and a lot of the manmade contributions to climate change are due to the we harvest and use sources of energy such as fossil fuels.
                    <br>
                    Sustainable farming places an emphasis on using renewable resources where possible - wind and solar are two great examples. Solar panels can be used to power electric fences and wind turbines can be used to capture energy. Running rivers also provide a source of hydroelectric power.
                </p>
    
                <h3>2. Natural Pest Management</h3>
                <p>Sometimes referred to as integrated pest management, this practice is about avoiding artificial and chemical pesticides, and instead allowing natural predators to control pest populations. Birds, lizards and insects can all play a role in keeping pests at bay.
                    <br>
                    It’s also important to note that not all insects are harmful to a farm. 
                </p>

                <h3>3. Hydroponics and Aquaponics</h3>
                <p>These two innovative techniques change the way crops are grown by removing the need for soil, instead providing nutrients directly to the plant through the water that the roots are grown in. In hydroponics, root systems are placed directly in a specialised mineral solution, or sometimes they are grown in gravel. The nutrients needed by the plant are added to the water given to the plant, removing the need for soil.
                    <br>
                    Aquaponics combines raising aquatic animals with the growing of hydroponic crops. In this approach, the water from the animal is used to nourish the plants. Since the water contains nutrients from the animal waste, it can be used to feed the plants. It is then recycled back into the system to be used once again by the fish.
                    <br>
                    Both these systems focus on sustainability through improving the efficiency of inputs.
                </p>

                <h3>4. Crop Rotation</h3>
                <p>Crop rotation is an important aspect of organic farming and has serious sustainability benefits. Crop rotation is the practice of growing different crops each season, with the new crops being used to replenish the nutrients the previous crops used. This practice dates all the way back to when farming was first being used.
                    <br>
                    Crop rotation also plays a role in pest control. Since certain pests are attracted to certain plants, changing up what is grown each season prevents pest populations from growing too fast or getting out of control.  
                </p>

                <h3>5. Polycultures</h3>
                <p>Polycultures involve growing multiple crops in the same area. The chosen species usually complement each other, and by growing multiple varieties farmers can better utilise available resources. It also improves resilience and promotes soil health.
                </p>

                <h3>6. Permaculture</h3>
                <p>On the other end of the spectrum to crop rotation is permaculture, which is a deliberate system of growing perennial crops such as fruit trees, nut trees and shrubs. The goal is to build a local ecosystem that mimics the way these species would be found in the wild. This system is used as a way to reduce waste, reduce inputs and maximise growing efficiency and crop yields.
                </p>

                <h3>7. Biodynamic Farming</h3>
                <p>Biodynamic farming is the practice of raising livestock alongside plants and crops, managing the farm as one intertwined system where the needs of each species supports the other. Biodynamic farms promote soil health and resilience, since they mimic the way plants and animals exist naturally. Biodynamics emphasizes the importance of reducing the use of off-site inputs (such as importing soil fertility) by generating the necessary health and soil fertility for food production onsite. This is achieved through the implementation of practices such as composting, application of animal manure from farmed animals, cover cropping or rotating complementary crops.
                </p>

                <h3>8. Urban Agriculture</h3>
                <p>The practice of urban agriculture has exploded in recent years, with advances in farming practices making at home agriculture more accessible than ever before.
                    <br>
                    Urban agriculture involves things like rooftop gardens, community gardens, indoor hydroponic farms and even warehouse farms where food can be produced on huge scales. 
                </p>

                <h3>9. Agroforestry</h3>
                <p>Agroforestry has been referred to as one of the most powerful ways we can prevent desertification.
                    <br>
                    The practice involves planting trees and shrubs amongst crops, to create a favorable microclimate that maintains favorable temperature and soil humidity, while protecting crops from wind or heavy rain. Trees have another important role. They stabilize soils, minimize nutrient runoff and improve soil structure.
                </p>

                <h3>10. Mulching and Groundcovers</h3>
                <p>It’s possible to dramatically reduce the growth of weeds and conserve soil by using mulch and ground covers around plants. This form of natural weed suppressant can, in some cases, eliminate the need for herbicides, and the most stubborn weeds that appear can easily be removed by hand.
                    <br>
                    Organic mulch material like, for example, wood chips, straw or grass clippings also improves nutrient retention in soils and encourages the activity of soil microorganisms that help create healthy aerated soil structure. This reduces the need for tillage as soils are less compacted.
                </p>
